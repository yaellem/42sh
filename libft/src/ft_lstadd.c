#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	if (*alst && new)
	{
		if ((*alst)->prev == NULL)
			(*alst)->prev = new;
		new->next = *alst;
		*alst = new;
	}
}
