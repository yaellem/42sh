#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	int				i;
	unsigned char	*dest;
	unsigned char	*srce;

	i = 0;
	dest = (unsigned char *)dst;
	srce = (unsigned char *)src;
	while (n-- > 0)
		dest[i++] = *srce++;
	return ((void *)dest);
}
