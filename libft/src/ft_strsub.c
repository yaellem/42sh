#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*dest;
	char	*sa;

	sa = NULL;
	if (s)
	{
		if ((dest = ft_strnew(len)) == NULL)
			return (0);
		sa = dest;
		while (len--)
			*dest++ = s[start++];
	}
	return (sa);
}
