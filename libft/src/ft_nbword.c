#include "libft.h"

int		ft_nbword(char *s)
{
	int		c;

	c = 0;
	if (s)
	{
		while (*s)
		{
			s = ft_pfrontws(s);
			while ((*s < 9 || *s > 13) && *s != 32 && *s)
				s++;
			if ((*(s - 1) < 9 || *(s - 1) > 13) && *(s - 1) != 32)
				c++;
		}
	}
	return (c);
}
