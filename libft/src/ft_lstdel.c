#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list		*s;

	s = *alst;
	while (s)
	{
		del((s)->content, (s)->content_size);
		free(s);
		s = (s)->next;
	}
	*alst = NULL;
}
