#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	*str;

	str = (unsigned char *)s;
	while (*str)
	{
		if (*str == (unsigned char)c)
			return ((void *)str);
		str++;
	}
	if ((unsigned char)c == '\0')
		return ((void *)str);
	return (0);
}
