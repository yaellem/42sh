#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t len)
{
	size_t c;

	c = -1;
	while ((++c < len) && src[c])
		dest[c] = src[c];
	while (c < len)
		dest[c++] = '\0';
	return (dest);
}
