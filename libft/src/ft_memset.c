#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int				i;
	unsigned char	*b1;

	i = 0;
	b1 = (unsigned char *)b;
	while (len-- > 0)
		b1[i++] = c;
	return ((void *)b1);
}
