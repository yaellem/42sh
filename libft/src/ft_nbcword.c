#include "libft.h"

int		ft_nbcword(char *s, char c)
{
	int		i;

	i = 0;
	if (s)
	{
		while (*s)
		{
			while (*s == c)
				s++;
			while (*s != c && *s)
				s++;
			if (*(s - 1) != c)
				i++;
		}
	}
	return (i);
}
